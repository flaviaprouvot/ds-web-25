// Adição, Subtração, Multiplicação e Divisão (fazer variáveis diferentes para todas as operações)

var n1 = Number.parseInt  (prompt("Soma: Digite o primeiro número "));
var n2 = Number.parseInt (prompt("Digite o segundo número para somar ao primeiro"));
alert(n1 + n2);

var n3 = Number.parseInt  (prompt("Sutração: Digite o primeiro número"));
var n4 = Number.parseInt (prompt("Digite o segundo número para subtrair do primeiro"));
alert(n3 - n4);

var n5 = Number.parseInt  (prompt("Multiplicação: Digite o primeiro número"));
var n6 = Number.parseInt (prompt("Digite o segundo número para multiplicar o primeiro"));
alert(n5 * n6);

var n7 = Number.parseInt  (prompt("Divisão: Digite o primeiro número"));
var n8 = Number.parseInt (prompt("Digite o segundo número para dividir o primeiro"));
alert(n7 / n8);